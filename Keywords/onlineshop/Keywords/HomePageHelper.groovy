class HomePageHelper {

	@Keyword
	def openBrowser(String countryId) {
		String link = GlobalHelper.getMapperByCountryId(countryId, "link");
		WebUI.openBrowser(link);
		WebUI.maximizeWindow();
		acceptCookies();
	}

	@Keyword
	def closeBrowser() {
		WebUI.closeBrowser();
	}

	def acceptCookies() {
		WebElement web = WebUI.findWebElement(findTestObject('Onlineshop.Pages/HomePage/Elements/ButtonCookies'));
		web.click();
	}
}